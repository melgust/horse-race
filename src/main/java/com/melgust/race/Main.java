    @Override
    public void update(Observable o, Object arg) {
        Horse horse = (Horse)o;
        int percentage = (int)arg;
        switch(horse.getId()) {
            case 1:
                this.lblProgressH1.setText("Progreso: " + percentage);
                this.jpbHorse1.setValue(percentage);
                break;
            case 2:
                this.lblProgressH2.setText("Progreso: " + percentage);
                this.jpbHorse2.setValue(percentage);
                break;
            case 3:
                this.lblProgressH3.setText("Progreso: " + percentage);
                this.jpbHorse3.setValue(percentage);
                break;
            case 4:
                this.lblProgressH4.setText("Progreso: " + percentage);
                this.jpbHorse4.setValue(percentage);
                break;
        }
        if (percentage >= 100) {
            stop();
            this.btnReset.setEnabled(true);
            this.btnStart.setEnabled(true);
            this.lblWinner.setText("Ganador: " + horse.getName());
        }
    }
