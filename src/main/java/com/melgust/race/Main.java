    private void btnStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStartActionPerformed
        // TODO add your handling code here:
        int minimun = Integer.parseInt(this.txtBegin.getText());
        int maximun = Integer.parseInt(this.txtEnd.getText());
        int time = Integer.parseInt(this.txtTime.getText()) * 1000;
        this.btnStart.setEnabled(false);
        this.btnReset.setEnabled(false);
        this.lblWinner.setText("");
        for (int j = 0; j < threads.length; j++) {
            Horse horse = new Horse("Caballo " + (j + 1), minimun, maximun, j + 1, time);
            horse.addObserver(this);
            threads[j] = new Thread(horse);
            threads[j].start();
        }
    }//GEN-LAST:event_btnStartActionPerformed

